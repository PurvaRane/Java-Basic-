public class Percentage
{
public static void main(String[] args)
{
double per = 92;
if(per>=75){
System.out.println("Distinction");
}
else if(per>=60){
System.out.println("1st Class");
}
else if(per>=50){
System.out.println("2nd Class");
}
else if(per>=40){
System.out.println("3rd Class");
}
else
{
System.out.println("Fail");
}
}
}